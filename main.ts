function create_level () {
    sprites.destroyAllSpritesOfKind(SpriteKind.Food)
    sprites.destroyAllSpritesOfKind(SpriteKind.Enemy)
    col = 1
    row = 1
    while (row <= 6) {
        col = 1
        while (col <= 8) {
            plaats_vloer(col, row)
            col += 1
        }
        row += 1
    }
    trappen_locatie = [randint(1, 8), randint(1, 6)]
    tiles.setTileAt(tiles.getTileLocation(trappen_locatie[0], trappen_locatie[1]), assets.tile`stairSouth`)
    tiles.setWallAt(tiles.getTileLocation(trappen_locatie[0], trappen_locatie[1]), false)
    powerup = sprites.create(img`
        . . . . c c c b b b b b . . . . 
        . . c c b 4 4 4 4 4 4 b b b . . 
        . c c 4 4 4 4 4 5 4 4 4 4 b c . 
        . e 4 4 4 4 4 4 4 4 4 5 4 4 e . 
        e b 4 5 4 4 5 4 4 4 4 4 4 4 b c 
        e b 4 4 4 4 4 4 4 4 4 4 5 4 4 e 
        e b b 4 4 4 4 4 4 4 4 4 4 4 b e 
        . e b 4 4 4 4 4 5 4 4 4 4 b e . 
        8 7 e e b 4 4 4 4 4 4 b e e 6 8 
        8 7 2 e e e e e e e e e e 2 7 8 
        e 6 6 2 2 2 2 2 2 2 2 2 2 6 c e 
        e c 6 7 6 6 7 7 7 6 6 7 6 c c e 
        e b e 8 8 c c 8 8 c c c 8 e b e 
        e e b e c c e e e e e c e b e e 
        . e e b b 4 4 4 4 4 4 4 4 e e . 
        . . . c c c c c e e e e e . . . 
        `, SpriteKind.Food)
    powerup_locatie_y = randint(1, 8)
    powerup_locatie_x = randint(1, 6)
    while (tiles.tileAtLocationIsWall(tiles.getTileLocation(powerup_locatie_y, powerup_locatie_x)) || tiles.tileAtLocationEquals(tiles.getTileLocation(powerup_locatie_y, powerup_locatie_x), assets.tile`stairSouth`)) {
        powerup_locatie_y = randint(1, 8)
        powerup_locatie_x = randint(1, 6)
    }
    powerup.setPosition(tiles.getTileLocation(powerup_locatie_y, powerup_locatie_x).x, tiles.getTileLocation(powerup_locatie_y, powerup_locatie_x).y)
    vijand = sprites.create(img`
        ........................
        ........................
        ........................
        ........................
        ..........ffff..........
        ........ff1111ff........
        .......fb111111bf.......
        .......f11111111f.......
        ......fd11111111df......
        ......fd11111111df......
        ......fddd1111dddf......
        ......fbdbfddfbdbf......
        ......fcdcf11fcdcf......
        .......fb111111bf.......
        ......fffcdb1bdffff.....
        ....fc111cbfbfc111cf....
        ....f1b1b1ffff1b1b1f....
        ....fbfbffffffbfbfbf....
        .........ffffff.........
        ...........fff..........
        ........................
        ........................
        ........................
        ........................
        `, SpriteKind.Enemy)
    if (Math.percentChance(25)) {
        tiles.placeOnRandomTile(vijand, sprites.dungeon.greenOuterWest1)
    } else if (Math.percentChance(25)) {
        tiles.placeOnRandomTile(vijand, sprites.dungeon.greenOuterEast0)
    } else if (Math.percentChance(25)) {
        tiles.placeOnRandomTile(vijand, sprites.dungeon.greenOuterNorth0)
    } else {
        tiles.placeOnRandomTile(vijand, sprites.dungeon.greenOuterSouth0)
    }
    info.startCountdown(3)
    vijand.follow(mySprite, 15)
}
sprites.onOverlap(SpriteKind.Player, SpriteKind.Food, function (sprite, otherSprite) {
    powerup.destroy(effects.spray, 100)
    mySprite.sayText("nomnom", 500, true)
    info.changeLifeBy(10)
    info.changeCountdownBy(2)
    tiles.setTileAt(speler_tilemap_locatie(), sprites.dungeon.floorLight2)
})
function plaats_vloer (col: number, row: number) {
    if (Math.percentChance(1)) {
        tiles.setTileAt(tiles.getTileLocation(col, row), assets.tile`myTile`)
        tiles.setWallAt(tiles.getTileLocation(col, row), false)
    } else if (Math.percentChance(10)) {
        tiles.setTileAt(tiles.getTileLocation(col, row), assets.tile`floorLight3`)
        tiles.setWallAt(tiles.getTileLocation(col, row), true)
    } else {
        tiles.setTileAt(tiles.getTileLocation(col, row), sprites.dungeon.floorMixed)
        tiles.setWallAt(tiles.getTileLocation(col, row), false)
    }
}
